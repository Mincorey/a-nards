/** Текущий баланс кошелька в рублях (заглушка до подключения платежей). */
export const WALLET_BALANCE_RUB = 3500;

/** Форматирует сумму в рублях со знаком ₽ (разделители тысяч — по-русски). */
export function formatRub(value: number): string {
  return `${value.toLocaleString('ru-RU')} ₽`;
}
