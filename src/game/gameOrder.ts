/* =============================================================================
 * gameOrder.ts — Чистая логика «какая строка партии новее» для онлайна.
 * -----------------------------------------------------------------------------
 * Проблема, которую решает (баг «фантомного второго броска костей»): при ходе
 * цепочкой (оба кубика одной шашкой) клиент ОПТИМИСТИЧНО применяет всю цепочку
 * сразу, а серверу отправляет полуходы по очереди. БД после КАЖДОГО полухода
 * рассылает realtime-строку; строка ПЕРВОГО полухода («ход всё ещё мой, кости
 * на столе») имеет updated_at СВЕЖЕЕ оптимистичного состояния и при сравнении
 * только по времени перетирала его — UI на миг откатывался назад, rollId
 * менялся, и Dice3D проигрывал повторный «кувырок» костей (фантомный бросок).
 *
 * Поэтому строки одной и той же партии сравниваем по ЛОГИЧЕСКОМУ ПРОГРЕССУ:
 *   1) ply (растёт при каждой передаче хода) — больше = новее;
 *   2) при равном ply: кости брошены (rolled) = новее, чем не брошены;
 *   3) при равном ply и брошенных костях: МЕНЬШЕ оставшихся кубиков = новее
 *      (полуходы внутри одного хода расходуют кубики);
 *   4) при полном равенстве прогресса — по updated_at (страховка от дублей
 *      broadcast/postgres_changes).
 * Строка со status='finished' всегда новее незавершённой (сдача/таймаут могут
 * завершить партию без продвижения ply). Строки ДРУГОЙ партии (новый id) —
 * всегда принимаются: сравнивать прогресс двух разных партий бессмысленно.
 * ========================================================================== */
import type { GameRow } from '../lib/online.types';

/** Поля, достаточные для сравнения (удобно в тестах). */
export type GameRowOrd = Pick<GameRow, 'id' | 'status' | 'ply' | 'rolled' | 'updated_at'> & {
  state: { dice: number[] };
};

/** Логический ранг строки внутри партии: растёт с каждым событием (бросок/ход/пас). */
function ord(r: GameRowOrd): number {
  return r.ply * 2 + (r.rolled ? 1 : 0);
}

/**
 * Принимать ли входящую строку `row` поверх текущей `cur`?
 * true — row логически новее (или это другая/завершившаяся партия);
 * false — row из «прошлого» (например, промежуточный полуход нашей же цепочки).
 */
export function isNewerGameRow(cur: GameRowOrd | null, row: GameRowOrd): boolean {
  if (!cur) return true;
  if (row.id !== cur.id) return true; // новая партия (реванш) — всегда принимаем
  if (cur.status === 'finished') {
    // Партия уже завершена — принимаем только дубль завершения посвежее.
    return row.status === 'finished'
      && Boolean(row.updated_at && cur.updated_at && row.updated_at > cur.updated_at);
  }
  if (row.status === 'finished') return true; // сдача/таймаут — принимаем всегда
  const oc = ord(cur);
  const orow = ord(row);
  if (orow !== oc) return orow > oc;
  // Одинаковый ply и «кости брошены»: сравниваем расход кубиков.
  const curLeft = cur.state?.dice?.length ?? 0;
  const rowLeft = row.state?.dice?.length ?? 0;
  if (rowLeft !== curLeft) return rowLeft < curLeft; // меньше осталось = новее
  // Полное равенство прогресса — по времени (отсекает дубли второго канала).
  return Boolean(row.updated_at && cur.updated_at && row.updated_at > cur.updated_at);
}
