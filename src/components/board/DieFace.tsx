/* =============================================================================
 * DieFace.tsx — маленькая «настоящая» игральная кость с точками (не эмодзи,
 * не SVG внутри доски) для использования в обычной HTML-разметке — например,
 * в статусной плашке вступительного броска «кто ходит первым» (BotGame.tsx).
 * Та же раскладка точек, что и на реальной доске (см. board/Dice.tsx).
 * ========================================================================== */
const PIPS: Record<number, [number, number][]> = {
  1: [[0.5, 0.5]],
  2: [[0.28, 0.28], [0.72, 0.72]],
  3: [[0.26, 0.26], [0.5, 0.5], [0.74, 0.74]],
  4: [[0.28, 0.28], [0.72, 0.28], [0.28, 0.72], [0.72, 0.72]],
  5: [[0.26, 0.26], [0.74, 0.26], [0.5, 0.5], [0.26, 0.74], [0.74, 0.74]],
  6: [[0.28, 0.24], [0.72, 0.24], [0.28, 0.5], [0.72, 0.5], [0.28, 0.76], [0.72, 0.76]],
};

export default function DieFace({ value, size = 30 }: { value: number; size?: number }) {
  return (
    <span className="die-face" style={{ width: size, height: size }}>
      {(PIPS[value] ?? []).map(([x, y], i) => (
        <span key={i} className="die-face__pip" style={{ left: `${x * 100}%`, top: `${y * 100}%` }} />
      ))}
    </span>
  );
}
