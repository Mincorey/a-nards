-- A-NARDS · Аудит H2 — Удаление мёртвой колонки games.seed (очистка).
-- -----------------------------------------------------------------------------
-- Броски теперь генерируются криптостойким ГСЧ (secureRng) в Edge Function
-- roll-dice, а start-game больше НЕ пишет seed. Колонка games.seed стала мёртвой
-- (default 0) и ничего не предсказывает.
--
-- ⚠️ ПОРЯДОК ПРИМЕНЕНИЯ: применять ТОЛЬКО ПОСЛЕ редеплоя обновлённых функций
-- roll-dice и start-game (иначе старые задеплоенные версии ещё обращаются к
-- колонке seed). После редеплоя — безопасно.
--
-- Идемпотентно.
-- =============================================================================
alter table public.games drop column if exists seed;
