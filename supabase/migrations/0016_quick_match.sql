-- =============================================================================
-- A-NARDS · Быстрый матчмейкинг: флаг быстрых столов (очередь поиска соперника).
-- -----------------------------------------------------------------------------
-- Кнопка «Играть» в лобби подбирает соперника автоматически: два игрока,
-- нажавших «Играть», садятся за один быстрый стол. Быстрые столы (quick=true)
-- служат «очередью поиска» и НЕ показываются в обычном списке лобби.
-- Идемпотентно.
-- =============================================================================
alter table public.game_tables
  add column if not exists quick boolean not null default false;

-- Индекс под подбор: открытые быстрые столы нужного варианта, по времени создания.
create index if not exists idx_game_tables_quick_waiting
  on public.game_tables (variant, created_at)
  where status = 'waiting' and quick = true;
